
import java.util.*;
public class Main
{
    public static void main(String[] args)
    {
// decresing star pattern
        for (int i=1;i<=5;i++)
        {
            for (int j=1;j<=5;j++)
        {
           if(i<=j)
               System.out.print("*");
           else
               System.out.print(" ");
        }
            System.out.println("");
        }
         }
           }

